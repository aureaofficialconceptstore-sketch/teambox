-- Un canvas condiviso e privato per ciascuna chat diretta 1:1.
create table if not exists public.direct_canvases (
  thread_id bigint primary key references public.direct_threads(id) on delete cascade,
  title text not null default 'Appunti condivisi' check (char_length(title) between 1 and 100),
  content jsonb not null default '{"notes":"","tables":[]}'::jsonb,
  updated_by uuid not null references public.profiles(id) on delete cascade,
  updated_at timestamptz not null default now()
);

alter table public.direct_canvases enable row level security;
drop policy if exists "Direct members read canvas" on public.direct_canvases;
drop policy if exists "Direct members create canvas" on public.direct_canvases;
drop policy if exists "Direct members update canvas" on public.direct_canvases;
create policy "Direct members read canvas" on public.direct_canvases for select to authenticated using (public.is_direct_thread_member(thread_id));
create policy "Direct members create canvas" on public.direct_canvases for insert to authenticated with check (updated_by = auth.uid() and public.is_direct_thread_member(thread_id));
create policy "Direct members update canvas" on public.direct_canvases for update to authenticated using (public.is_direct_thread_member(thread_id)) with check (updated_by = auth.uid() and public.is_direct_thread_member(thread_id));

