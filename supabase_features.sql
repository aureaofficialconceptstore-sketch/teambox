-- Canali con membri scelti dal creatore.
create table if not exists public.channel_members (
  channel_id bigint not null references public.channels(id) on delete cascade,
  profile_id uuid not null references public.profiles(id) on delete cascade,
  added_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  primary key (channel_id, profile_id)
);

-- Ogni nuovo membro vede subito il canale generale; gli altri canali restano
-- selezionabili dal loro creatore nella finestra "People in channel".
create or replace function public.create_profile_for_new_user()
returns trigger language plpgsql security definer set search_path = public as $$
begin
  insert into public.profiles (id, display_name, avatar_color)
  values (new.id, coalesce(nullif(new.raw_user_meta_data ->> 'display_name', ''), split_part(new.email, '@', 1)), 'violet')
  on conflict (id) do nothing;

  insert into public.channel_members (channel_id, profile_id, added_by)
  select channel.id, new.id, null
  from public.channels channel
  where lower(channel.slug) in ('general', 'generale')
  on conflict do nothing;
  return new;
end;
$$;

alter table public.channel_members enable row level security;

create or replace function public.is_channel_member(target_channel_id bigint)
returns boolean language sql security definer stable set search_path = public as $$
  select exists (
    select 1 from public.channel_members
    where channel_id = target_channel_id and profile_id = auth.uid()
  );
$$;

create or replace function public.is_channel_owner(target_channel_id bigint)
returns boolean language sql security definer stable set search_path = public as $$
  select exists (
    select 1 from public.channels
    where id = target_channel_id and created_by::text = auth.uid()::text
  );
$$;

create or replace function public.create_team_channel(
  channel_slug text,
  channel_description text,
  invited_profile_ids uuid[] default array[]::uuid[]
)
returns table (id bigint, slug text, description text, created_at timestamptz)
language plpgsql security definer set search_path = public as $$
declare
  new_channel_id bigint;
begin
  if auth.uid() is null then raise exception 'Devi accedere prima di creare un canale.'; end if;
  if channel_slug !~ '^[a-z0-9-]{1,48}$' then raise exception 'Nome del canale non valido.'; end if;

  insert into public.channels (slug, description, created_by)
  values (channel_slug, coalesce(nullif(channel_description, ''), 'Un nuovo spazio per il team'), auth.uid())
  returning channels.id into new_channel_id;

  insert into public.channel_members (channel_id, profile_id, added_by)
  values (new_channel_id, auth.uid(), auth.uid())
  on conflict do nothing;

  insert into public.channel_members (channel_id, profile_id, added_by)
  select new_channel_id, profile.id, auth.uid()
  from public.profiles profile
  where profile.id = any(coalesce(invited_profile_ids, array[]::uuid[]))
  on conflict do nothing;

  return query select channels.id, channels.slug, channels.description, channels.created_at
  from public.channels where channels.id = new_channel_id;
end;
$$;

create or replace function public.add_channel_members(
  target_channel_id bigint,
  invited_profile_ids uuid[]
)
returns void language plpgsql security definer set search_path = public as $$
begin
  if auth.uid() is null or not public.is_channel_owner(target_channel_id) then
    raise exception 'Solo chi ha creato il canale può aggiungere persone.';
  end if;
  insert into public.channel_members (channel_id, profile_id, added_by)
  select target_channel_id, profile.id, auth.uid()
  from public.profiles profile
  where profile.id = any(coalesce(invited_profile_ids, array[]::uuid[]))
  on conflict do nothing;
end;
$$;

-- Mantiene utilizzabili i canali già creati aggiungendo gli attuali membri del workspace.
insert into public.channel_members (channel_id, profile_id, added_by)
select
  channel.id,
  profile.id,
  case when channel.created_by::text = profile.id::text then profile.id else null end
from public.channels channel cross join public.profiles profile
on conflict do nothing;

