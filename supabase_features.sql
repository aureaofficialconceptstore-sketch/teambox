create table if not exists public.direct_threads (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now()
);

create table if not exists public.direct_thread_members (
  thread_id bigint not null references public.direct_threads(id) on delete cascade,
  profile_id uuid not null references public.profiles(id) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (thread_id, profile_id)
);

create table if not exists public.direct_messages (
  id bigint generated by default as identity primary key,
  thread_id bigint not null references public.direct_threads(id) on delete cascade,
  author_id uuid not null references public.profiles(id) on delete cascade,
  body text not null check (char_length(body) between 1 and 4000),
  created_at timestamptz not null default now()
);

create index if not exists direct_messages_thread_created_at_idx on public.direct_messages(thread_id, created_at);

create table if not exists public.message_attachments (
  id bigint generated by default as identity primary key,
  message_id bigint not null references public.messages(id) on delete cascade,
  file_name text not null check (char_length(file_name) between 1 and 255),
  file_path text not null unique,
  mime_type text,
  byte_size bigint not null check (byte_size >= 0),
  created_by uuid not null references public.profiles(id) on delete cascade,
  created_at timestamptz not null default now()
);

alter table public.direct_threads enable row level security;
alter table public.direct_thread_members enable row level security;
alter table public.direct_messages enable row level security;
alter table public.message_attachments enable row level security;

create or replace function public.is_direct_thread_member(target_thread_id bigint)
returns boolean language sql security definer stable set search_path = public as $$
  select exists (
    select 1 from public.direct_thread_members
    where thread_id = target_thread_id and profile_id = auth.uid()
  );
$$;

create or replace function public.get_or_create_direct_thread(other_profile_id uuid)
returns bigint language plpgsql security definer set search_path = public as $$
declare
  existing_thread_id bigint;
  new_thread_id bigint;
begin
  if auth.uid() is null then raise exception 'Devi accedere prima di avviare una chat diretta.'; end if;
  if other_profile_id = auth.uid() then raise exception 'Non puoi aprire una chat diretta con te stessa/o.'; end if;

  select mine.thread_id into existing_thread_id
  from public.direct_thread_members mine
  join public.direct_thread_members other on other.thread_id = mine.thread_id
  where mine.profile_id = auth.uid() and other.profile_id = other_profile_id
  limit 1;
  if existing_thread_id is not null then return existing_thread_id; end if;

  insert into public.direct_threads default values returning id into new_thread_id;
  insert into public.direct_thread_members (thread_id, profile_id)
  values (new_thread_id, auth.uid()), (new_thread_id, other_profile_id);
  return new_thread_id;
end;
$$;

